        <h1><a name="_Toc30236297"></a>6. CleanCode</h1>
        <p><img src="<?=DOCUMENTATION_IMAGE_PATH.$i.'.png'; $i++;?>" alt="Hier kommt ein bild" /></p>
        <p>Wir haben versucht in diesem Projekt einen CodeStyle anzuwenden und uns hier an dem CleanCode gerichtet, welches von Robert C. Martin hervorgebracht wurde.</p>
        <p>Hierzu haben wir auch einen Vortrag zu dem Thema Besucht und uns auch in die wichtigsten Details eingelesen.</p>
        <p>Die wichtigsten Punkte, welche wir versucht haben umzusetzen:</p>
        <ul>
            <li>Sprechende / beschreibende Funktionsnamen</li>
            <li>Methodennamen sollen zum Verhalten der Methode passen</li>
            <li>Methoden Inhalt in kleine Methoden aufteilen.</li>
            <li>Kommentare nur dort wo es zwingend notwendig ist</li>
            <li>DRY: Don&rsquo;t Repeat Yourself</li>
            <li>KISS: Keep it Simple, Stupid!</li>
        </ul>
        <p>Hier sind wir nat&uuml;rlich immer wieder &uuml;ber einige H&uuml;rden gestolpert, konnten es zum Ende hin aber recht gut umsetzen. Mit steigender Erfahrung ist hier aber auf jeden Fall noch viel heraus zu holen.</p>

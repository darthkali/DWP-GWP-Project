        <h3><a name="_Toc30236251"></a>2.1 Ideenfindung</h3>
        <p>Zun&auml;chst war unser Gedanke, dass wir eines der beiden Projektideen aus dem letzten Semester umsetzen wollten (aus dem Kurs DB2 / SWT1). Dennoch haben wir eine Brainstormrunde gemacht und Ideen aufgeschrieben.</p>

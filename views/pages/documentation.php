        <h3><a name="_Toc30236252"></a>2.2 Ausgangssituation</h3>
        <p>Der Fachschaftsrat der Angewandten Informatik veranstaltet einige Events im Jahr und nutzt zur Verbreitung der Ank&uuml;ndigungen zurzeit verschiedene Externe Quellen (Twitter, Facebook, &hellip;). Neben den Events sind wir auch Ansprechpartner f&uuml;r viele Studierende. Jedoch fehlt hier ein Zentraler Anlaufpunkt f&uuml;r Interessierte und Nutzer. Dies soll nun mit Hilfe einer eigenen Webseite f&uuml;r den Fachschaftsrat der Angewandten Informatik geschehen. Ebenso soll der FSR auch ein wenig besser nach au&szlig;en Pr&auml;sentiert werden.</p>

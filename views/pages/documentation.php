        <h1><a name="_Toc30236282"></a>4. Funktionalit&auml;ten</h1>
        <p>Wir haben 2 wichtige Zuordnungen eines Nutzers.</p>
        <p>Die Rolle (Webseite) und die Funktion-FSR (Funktion im Fachschaftsrat)</p>
        <p>Die <strong>Rolle</strong> dient der Rechtevergabe auf der Webseite und hat mit dem Fachschaftsrat selber nichts zu tun. Ein Administrator kann durchaus jemand sein, der nie im FSR Mitglied war.</p>
        <p>Die <strong>Funktion-FSR</strong> zeigt die aktuelle bzw. vergangene Funktion eines Mitgliedes des FSR. Also war er nur ein einfaches Mitglied, oder z.B. Sprecher, Finanzer, &hellip; .</p>
        <h3><a name="_Toc30236283"></a>4.1 Rollen</h3>
        <p>Wir haben auf der Seite 3 Verschiedene Rollen. Den Administrator (ADMIN), das Mitglied (MEMBER) und den Nutzer (USER).</p>
        <h3><a name="_Toc30236284"></a>4.1.1 Nutzer (USER)</h3>
        <p>Der Nutzer kann nur sein Profil bearbeiten und sich f&uuml;r Events an- und abmelden. Um zu einem Nutzer zu werden muss man sich registrieren.</p>
        <h3><a name="_Toc30236285"></a>4.1.2 Mitglied (MEMBER)</h3>
        <p>Das Mitglied hat die gleichen Rechte wie der Nutzer, kann aber zus&auml;tzlich noch die Events verwalten.</p>
        <p>Um diese Zugriffsrechte zu bekommen, muss man sich zun&auml;chst als normaler Nutzer Registrieren und wird dann von einem Admin zu einem Mitglied hochgestuft.</p>
        <p>Wenn man einmal ein Mitglied war, hat man auch automatisch eine Mitglieder Historie. Wird man zu einem normalen Nutzer eingestuft, z.b: weil man nicht mehr offiziell im FSR Mitglied ist, dann gilt man als inaktives Mitglied. Ein inaktives Mitglied hat dieselben Rechte wie ein Normaler Nutzer.</p>
        <h3><a name="_Toc30236286"></a>4.1.3 Administrator (ADMIN)</h3>
        <p>Der Administrator hat den Vollen Zugriff auf die Seite. Er hat alle Rechte, die ein Mitglied besitzt und kann zus&auml;tzlich die Nutzer verwalten.</p>
        <p>Um diese Zugriffsrechte zu bekommen, muss man sich zun&auml;chst als normaler Nutzer Registrieren und wird dann von einem Administrator zu einem Administrator hochgestuft. Es gibt Standartm&auml;&szlig;ig einen eingerichteten Administrator, um diese Funktionalit&auml;t zu gew&auml;hrleiten.</p>
        <p>Wichtig: Der Administrator kann sich selber nicht l&ouml;schen. Somit haben wir gew&auml;hrleitet, dass es immer mindestens einen Administrator auf der Seite gibt.</p>

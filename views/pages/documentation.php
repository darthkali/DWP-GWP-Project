        <p>Kein Projekt ist wie das andere und so ergaben sich auch hier Sachverhalte und Erkenntnisse, die wir f&uuml;r unsere zuk&uuml;nftigen Projekte mitnehmen werden. Die wichtigsten Punkte die, in diesem Projekt f&uuml;r unser Team, aufkamen waren</p>
        <p>- der Projektentwurf und -Verlauf</p>
        <p>- Teamarbeit</p>
        <p>- Kundenkommunikation</p>
        <p>- Dokumentation</p>
        <p></p>
        <h3><strong>Projektentwurf und -verlauf</strong></h3>
        <p>Was uns aufgefallen ist, dass es mehr PHP und vor allem weniger HTML war als gedacht! Dadurch dass wir sehr viel HTML generieren und ein MVC nutzen, spielte sich das meiste mit PHP ab.</p>
        <p><img src="<?=DOCUMENTATION_IMAGE_PATH.$i.'.png'; $i++;?>" alt="Hier kommt ein bild" /></p>
        <p>Aufgrund des Lernprozesses haben wir die Seite vor allem Backendseitig &ouml;fter umgebaut. Vor allem die Umsetzung des MVC war ein gro&szlig;er Schritt. Trotz erster bedenken, ob der Nutzen dem Zeitaufwand des Umbaus gerechtfertigt ist, haben wir uns zu diesem Schritt entschieden (auf zuraten von Herrn Friess). Das hat sich zum Ende hin als sehr erfolgreich und wichtig herausgestellt. Hier konnten wir im Nachhinein sehr gut und schnell neue Funktionen einbauen und fachlich besser umsetzen.</p>
        <p>Bei der Validierung mussten wir erfahren, wie Komplex dieser Bereich ist. Alleine Die Hausnummer sorgt schon f&uuml;r einiges an Arbeit. So muss man hier nicht nur auf Minderst und Maximal L&auml;nge pr&uuml;fen, sondern auch dass die Nummer mit einer Zahl anf&auml;ngt und ggf. einen Buchstaben (gro&szlig; und Klein) zul&auml;sst. Wird ein Buchstabe angeh&auml;ngt, dann muss ein Leerzeichen dazwischen.</p>
        <p>Aus Zeitlichen Gr&uuml;nden haben wir uns entschieden einige der Validierungen hier etwas zu vereinfachen. Diese sind jedoch aus unserer Sicht nur dort vereinfacht, wo es nicht zwingend notwendig ist. Wie z.b. dass man bei der Nummer das Leerzeichen schreiben muss.</p>
        <h3><strong>Teamarbeit</strong></h3>
        <p>Bei Gelegenheiten, bei denen man sich nicht oder nur schlecht abgesprochen hatte, waren im Nachhinein immer gr&ouml;&szlig;ere Anpassungen an der bereits get&auml;tigten Arbeit n&ouml;tig. Hier wurde im Verlauf des Projektes mehr darauf geachtet, im Vorfeld Grundlegendes zu besprechen, was zu einer h&ouml;heren Effizienz f&uuml;hrte.</p>
        <p>Bereits in fr&uuml;heren Projekten hatte sich gezeigt, dass es sinnvoll ist, auch in kleinen Teams Aufgaben direkt zuzuweisen und mit einem Abgabetermin zu versehen. So wurde es auch in diesem Projekt wieder gehandhabt. Hierzu haben wir die Issues in GitHub genutzt.</p>
        <p>-Teamarbeit ist das A und O-</p>
        <p>Ebenso ist das Klima innerhalb des Projektteams ausschlaggebend f&uuml;r das erfolgreiche Abschlie&szlig;en eines Projekts. Ohne die gute Zusammenarbeit in unserem Projektteam h&auml;tten wir die Ziele in diesem Projekt nicht zeitgerecht und mit diesem guten Ergebnis erreicht. Es hat sich gezeigt, dass es sich lohnt, die eigene Arbeit durch die Teammitglieder &uuml;berpr&uuml;fen zu lassen. So fielen kleinere Fehler sofort auf und mussten nicht zum Ende des Projektes m&uuml;hsam korrigiert werden.</p>
        <p>Wir haben uns auch dazu entschlossen unser Projekt 2 anderen Teams vor der Abgabe zur Verf&uuml;gung zu stellen. Wir haben im Gegenzug das Projekt der jeweiligen Teams ebenfalls bekommen. Ziel war es den Installationsprozess zu vollziehen und die Seite dann zu Testen. Hier stellen sich noch einige Fehler heraus, die vor allem auf die andere bearbeitungsweise die Teams zur&uuml;ckzuf&uuml;hren sind. Hier konnte man also nochmal eine ganze Hand voll Fehler beseitigen.<br /> Dieses Vorgehen ist vor allem deshalb wichtig, da man bei seinem eigenen Projekt schnell betriebsblind wird und Fehler eher schlechter entdeckt.</p>
        <h3><strong>Kundenkommunikation</strong></h3>
        <p>Die Kommunikation &bdquo;Face to Face&ldquo;, egal ob es teamintern oder nach Au&szlig;en mit dem Kunden war, stellte sich immer als der bessere und schnellere Weg heraus. Schriftliche Kommunikation f&uuml;hrte schnell zu Missverst&auml;ndnissen, die sp&auml;ter doch wieder m&uuml;ndlich diskutiert werden mussten.</p>
        <p>Regelm&auml;&szlig;ige und vor allem rechtzeitige Meetings haben dazu gef&uuml;hrt, dass ein guter Informationsstand hergestellt wurde.</p>
        <h3><strong>Dokumentation</strong></h3>
        <p>Es hat sich gezeigt, dass die permanente Dokumentation die Projektarbeit enorm erleichterte. Jeder wusste zu jedem Zeitpunkt, welchen Stand das Projekt hat und was noch offen war. Auch die m&uuml;ndlichen Vereinbarungen wurden als Nachweis f&uuml;r Kunde und Projektteam immer direkt festgehalten.</p>
        <p><img src="<?=DOCUMENTATION_IMAGE_PATH.$i.'.png'; $i++;?>" alt="Hier kommt ein bild" /></p>
        <p>Eine einheitliche Struktur zu definieren und diese sp&auml;ter einzuhalten war nicht einfach. Hier sollten im Vorhinein bessere und detailliertere Absprachen getroffen werden. Dieser Aspekt kostete im sp&auml;teren Projektverlauf etwas Zeit.</p>
